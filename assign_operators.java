public class assign_operators {
   public static void main(String[] args) {
      int num = 11;

      switch (num) {
         case 1:
            System.out.println("Monday 1 2 3 4 5 6 7");

            break;
         case 2:
            System.out.println("Tuesday");
            break;
         default:
            System.out.println("Enter valid Number");

      }

   }
}
